// # Travel Planner with Itinerary + Expense Splitter

// ## 1. Background
// Group trips create confusion due to scattered plans, lost tickets, and messy expense splitting. This app provides one shared space for planning, storing documents, tracking expenses, and settling balances.

// ## 2. Challenge
// Build a collaborative travel app that supports real-time itinerary editing, shared expense logging, unequal splits, and simplified debt settlements.

// ## 3. User Roles
// - **Admin**: Creates trip, sets dates/destination, invites members, manages permissions, finalizes itinerary.
// - **Member**: Adds activities, uploads tickets/docs, logs expenses, views balances, settles payments.

// ## 4. Core Requirements
// ### Functional
// - Real-time collaborative itinerary (drag-and-drop timeline).
// - Expense splitting with equal/unequal shares.
// - Debt simplification: minimize number of transactions (Min-Cash-Flow algorithm).
// - Voting system for group decisions.
// - Document vault for tickets, vouchers, IDs.

// ### Non-Functional
// - Offline viewing + offline expense logging.
// - Auto currency conversion for foreign trips.
// - Instant sync across devices.

// ## 5. Technical Hints
// - **Frontend:** React Native / Flutter  
// - **Backend:** Node.js / Django  
// - **DB:** Firebase / PostgreSQL  
// - **Algorithms:** Min-Cash-Flow for settlements  
// - **APIs:** Google Places, OpenExchangeRates  

// ## 6. Deliverables
// - Create Trip → Add Members → Build Itinerary
// - Add Expense → Split → View Dashboard
// - Show debt simplification working
// - Display algorithm logic (diagram or explanation)
// - Offline demo (view data without network)

// ## 7. Judging Criteria
// UX/UI (25%), Logic (25%), Collaboration (20%), Utility (15%), Completeness (15%)

// ## 8. Outcome
// A complete travel companion that centralizes trip plans, documents, expenses, and settlements with smooth collaboration.
